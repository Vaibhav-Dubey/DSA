myTuple = (1,2,3,4)
myTuple1 =  (1,2,6,7,9)

# '+'operation

print(myTuple + myTuple1)

# '*' operation

print(myTuple * 4)

# in operation

print(4 in myTuple)

# count operation

print(myTuple.count(2))

#index opertion 

print(myTuple.index(3))

#len operation 

print(len(myTuple))

